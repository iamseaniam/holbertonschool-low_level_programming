/**
 * @brief This function recursively prints a string followed by a new line.
 *
 * This function takes a pointer to a string 's' and recursively prints each 
 * of the string followed by a new line until the end of the string is reached.
 *
 * @param s The string to print.
 */
void _puts_recursion(char *s)
{
	printf("%s\n", s);
}
